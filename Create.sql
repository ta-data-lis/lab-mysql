USE lab_mysql;

CREATE TABLE CARS (id INT , CAR_ID INT PRIMARY KEY, MANUFACTURER VARCHAR(20), MODEL VARCHAR(20), YEAR CHAR(4), COLOR VARCHAR(10), DATE_SOLD DATE );

CREATE TABLE CUSTOMER (id INT , CUSTOMER_ID INT  , NAME VARCHAR(30), ADDRESS VARCHAR(40), PHONE VARCHAR(20), CAR_ID INT , DATE_SOLD DATE, PRIMARY KEY(CUSTOMER_ID), FOREIGN KEY (CAR_ID) REFERENCES CARS(CAR_ID));

CREATE TABLE SALESPERSON (id INT , STAFF_ID INT , NAME VARCHAR(30), ADDRESS VARCHAR(40), OUTLET VARCHAR(20), PRIMARY KEY(STAFF_ID));

CREATE TABLE INVOICE (id INT , INVOICE_NR INT , INV_DATE DATE, CAR_ID INT, CUSTOMER_ID INT , STAFF_ID INT, PRIMARY KEY(INVOICE_NR),FOREIGN KEY(STAFF_ID) REFERENCES SALESPERSON(STAFF_ID), FOREIGN KEY (CAR_ID) REFERENCES CARS(CAR_ID));




