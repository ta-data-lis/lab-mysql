CREATE DATABASE
lab_actions;

CREATE TABLE IF NOT EXISTS
lab_actions.cars (ID TINYINT,VIN SMALLINT PRIMARY KEY, Manufacturer VARCHAR(40), Model VARCHAR(40), Year TINYINT, Colour VARCHAR(20));

CREATE TABLE IF NOT EXISTS
lab_actions.invoices (ID TINYINT, Inv_num SMALLINT PRIMARY KEY, Date DATETIME, 
VIN SMALLINT NOT NULL, CustomerID SMALLINT NOT NULL, StaffID SMALLINT NOT NULL);

CREATE TABLE IF NOT EXISTS
lab_actions.salespeople (ID TINYINT,StaffID SMALLINT PRIMARY KEY, Staff_Name VARCHAR(100), Store VARCHAR(100));

CREATE TABLE IF NOT EXISTS
lab_actions.customers (ID TINYINT, CustomerID SMALLINT PRIMARY KEY, Name VARCHAR(100), Phone_Number SMALLINT,
Email VARCHAR(70), Adress VARCHAR(100), City VARCHAR(70), State_Province VARCHAR(70), Country VARCHAR(100),
ZipCode TINYINT);
